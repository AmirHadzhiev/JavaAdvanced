package DefiningClassesExercise.Google;

public class Children {
    String childName;
    String childBirthay;

    public Children(String childName, String childBirthay) {
        this.childName = childName;
        this.childBirthay = childBirthay;
    }

    public String getChildName() {
        return childName;
    }

    public void setChildName(String childName) {
        this.childName = childName;
    }

    public String getChildBirthay() {
        return childBirthay;
    }

    public void setChildBirthay(String childBirthay) {
        this.childBirthay = childBirthay;
    }
}
