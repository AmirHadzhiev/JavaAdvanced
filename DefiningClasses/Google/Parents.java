package DefiningClassesExercise.Google;

public class Parents {
    String parentName;
    String parentBirthay;

    public Parents(String parentName, String parentBirthay) {
        this.parentName = parentName;
        this.parentBirthay = parentBirthay;
    }

    public String getParentName() {
        return parentName;
    }

    public void setParentName(String parentName) {
        this.parentName = parentName;
    }

    public String getParentBirthay() {
        return parentBirthay;
    }

    public void setParentBirthay(String parentBirthay) {
        this.parentBirthay = parentBirthay;
    }
}
